// Токоизмерительный резистор DS2438, Ом. Влияет на ток (мА) и пересчёт
// счётчиков ICA/CCA/DCA в мА·ч. Значение калибровочное — подберите под свою
// АКБ, чтобы ёмкость сходилась с паспортной (типично 20..40 мОм).
#define DS2438_RSENSE_OHM 0.025f
// Цена младшего разряда счётчиков заряда в мА·ч (даташит DS2438): 0.4882/Rsense.
#define DS2438_MAH_PER_LSB (0.4882f / DS2438_RSENSE_OHM)
// Паспортная ёмкость АКБ, мА·ч — для подсчёта циклов заряда/разряда:
// 1 цикл = суммарный заряд (разряд), равный одной полной ёмкости.
// Укажите ёмкость вашей батареи (PMNN4409 — 2500, PMNN4493 — 3000 и т.п.).
#define BATTERY_RATED_MAH 2500
// Полная шкала ICA (остаточной ёмкости DS2438), мА·ч: ICA=255 -> столько мА·ч.
// Используется для показа/правки остаточной ёмкости в мА·ч (вместо процентов).
#define ICA_FULL_MAH (255.0f * DS2438_MAH_PER_LSB)

